<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Canal;

class CanalController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('canals.index');
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('canals.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $nomCanal = new Canal([
            'nomC' => $request->get('nomC'),
        ]);
        $nomCanal->save();
        return redirect()->route('canalI');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit()
    {
        $idCanal1 = \DB::table('canals')->select('id','nomC')->get();
        
        return view('canals.edit', compact('idCanal1'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $this->validate($request,[
            'id'    =>  'required',
            'nomC' =>  'required'
        ]);
        $canal = Canal::find($request->get('id'));
        $canal->nomC = $request->get('nomC');
        $canal->save();
        return redirect()->route('canalI')->with('success','Data Updated');
    }

